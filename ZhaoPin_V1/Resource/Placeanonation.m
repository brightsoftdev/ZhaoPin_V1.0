#import "Placeanonation.h"

@implementation Placeanonation

@synthesize name,subTitle1,coordinatee;
- (id)initWithCoord:(CLLocationCoordinate2D)coor
{
    self = [super init];
    if (self) {
        coordinatee=coor;
    }
    
    return self;
}
-(CLLocationCoordinate2D)coordinate
{
    return coordinatee;
}
-(NSString *)title
{
    return name;
}
-(NSString *)subtitle
{
    return subTitle1;
}

@end
